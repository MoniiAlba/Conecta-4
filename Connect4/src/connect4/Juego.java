    public void checaColumna(int col){ //col va de 0 a 6        
        int fila = 5;
        if(matriz[fila][col] == -1){ // no hay nada
            //ponemos algo
            matriz[fila][col] = turno;
            cambiaTurno(matrizBotones[fila][col]);
        }else{
            boolean sigue=true;
            while(sigue){
                if(fila>0){
                    fila--;
                    if(matriz[fila][col]==-1){ //no hay algo                     
                        matriz[fila][col] = turno;
                        cambiaTurno(matrizBotones[fila][col]);
                        sigue = false;
                    }
                }else{
                    sigue = false;
                }
            }
        }
    }
  

